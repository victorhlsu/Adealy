/**
 * POST /api/data/attractions
 * Returns famous attractions within the specified city (excludes food/nightlife).
 *
 * Request body:
 * {
 *   "city": "Paris"   // required
 * }
 *
 * Success response:
 * {
 *   "status": "ok",
 *   "city": "Paris",
 *   "attractions": [
 *     {
 *       "name": "Eiffel Tower",
 *       "type": "landmark",
 *       "description": "~20 words on why it is famous",
 *       "latitude": 48.8584,
 *       "longitude": 2.2945,
 *       "opening_time": "09:30",
 *       "closing_time": "23:45",
 *       "cost_amount": 29.4,
 *       "cost_currency": "EUR",
 *       "cost_note": "adult ticket",
 *       "booking_required": true,
 *       "famous_for": "iron lattice tower and views"
 *     }
 *   ]
 * }
 *
 * Error responses:
 * { "status": "error", "message": "City name is required" }
 * { "status": "error", "message": "Invalid city name detected" }
 * { "status": "error", "message": "Failed to parse attractions data", "raw_response": "..." }
 */

const { Router } = require('express');
const ai = require('../../ai/ai');

const router = Router();

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const isValidLatLng = (lat, lng) => {
	if (typeof lat !== 'number' || typeof lng !== 'number') return false;
	if (!Number.isFinite(lat) || !Number.isFinite(lng)) return false;
	return lat >= -90 && lat <= 90 && lng >= -180 && lng <= 180;
};

async function fetchJson(url, options = {}) {
	if (typeof fetch === 'function') {
		const resp = await fetch(url, options);
		return resp;
	}

	// eslint-disable-next-line global-require
	const https = require('https');
	return new Promise((resolve, reject) => {
		const req = https.request(url, { method: options.method || 'GET', headers: options.headers || {} }, (res) => {
			let body = '';
			res.on('data', (chunk) => { body += chunk.toString('utf8'); });
			res.on('end', () => {
				resolve({
					ok: res.statusCode >= 200 && res.statusCode < 300,
					status: res.statusCode,
					json: async () => JSON.parse(body),
					text: async () => body,
				});
			});
		});
		req.on('error', reject);
		req.end();
	});
}

async function geocodeWithNominatim(query) {
	try {
		if (process.env.DISABLE_NOMINATIM === '1') return null;

		const q = String(query || '').trim();
		if (!q) return null;

		const params = new URLSearchParams({
			format: 'jsonv2',
			limit: '1',
			q,
		});
		if (process.env.NOMINATIM_EMAIL) params.set('email', String(process.env.NOMINATIM_EMAIL));

		const url = `https://nominatim.openstreetmap.org/search?${params.toString()}`;
		const resp = await fetchJson(url, {
			headers: {
				'User-Agent': process.env.NOMINATIM_USER_AGENT || 'AdealyHackCanada/0.1',
				'Accept': 'application/json',
			},
		});
		if (!resp.ok) return null;

		const data = await resp.json();
		if (!Array.isArray(data) || data.length === 0) return null;

		const first = data[0];
		const lat = Number.parseFloat(first?.lat);
		const lng = Number.parseFloat(first?.lon);
		if (!isValidLatLng(lat, lng)) return null;

		return { latitude: lat, longitude: lng, address: first?.display_name };
	} catch (e) {
		return null;
	}
}

async function enrichAttractionsWithNominatimCoordinates({ city, attractions }) {
	try {
		if (process.env.DISABLE_NOMINATIM === '1') return attractions;
		if (!Array.isArray(attractions) || attractions.length === 0) return attractions;

		// Attractions are generated by AI; always geocode them for accuracy (capped and rate-limited).
		const maxToGeocode = Math.min(attractions.length, Number(process.env.NOMINATIM_MAX_GEOCODES || 15));
		for (let i = 0; i < maxToGeocode; i++) {
			const a = attractions[i];
			if (!a || typeof a !== 'object') continue;
			const name = String(a.name || '').trim();
			if (!name) continue;

			if (i > 0) await sleep(1100);

			const q = `${name}, ${String(city || '').trim()}`;
			const geo = await geocodeWithNominatim(q);
			if (!geo) continue;
			a.latitude = geo.latitude;
			a.longitude = geo.longitude;
		}

		return attractions;
	} catch (err) {
		console.error('[attractions] Failed to enrich attractions with Nominatim coordinates:', err);
		return attractions;
	}
}

const ALLOWED_TYPES = [
	'landmark',
	'museum',
	'park',
	'garden',
	'viewpoint',
	'neighborhood',
	'market',
	'shopping',
	'cultural',
	'historical',
	'religious',
	'entertainment',
	'monument',
	'plaza',
	'beach',
];

const suspiciousPatterns = /ignore|system|role|prompt|instruction|execute|eval|script|<script|javascript:/i;
const foodPatterns = /(restaurant|cafe|bar|bistro|pub|bakery|coffee|food|eatery|diner|brasserie|nightlife)/i;

function titleCase(str) {
	if (!str) return str;
	return str
		.split(/\s+/)
		.map((w) => w.charAt(0).toUpperCase() + w.slice(1))
		.join(' ');
}

router.post('/', async (req, res) => {
	const { city } = req.body || {};

	if (!city || !String(city).trim()) {
		return res.status(400).json({ status: 'error', message: 'City name is required' });
	}

	try {
		const response = await ai.generate(city, 'ATTRACTIONS_PROMPT');

		if (response === '404') {
			return res.status(500).json({ status: 'error', message: '[attractions] Failed to query Gemini for attractions' });
		}

		let cleaned = response.trim();
		if (cleaned.startsWith('```json')) cleaned = cleaned.replace(/^```json\s*/, '').replace(/\s*```$/, '');
		else if (cleaned.startsWith('```')) cleaned = cleaned.replace(/^```\s*/, '').replace(/\s*```$/, '');

		let data;
		try {
			data = JSON.parse(cleaned);
		} catch (err) {
			console.error('[attractions] Failed to parse response:', cleaned);
			return res.status(500).json({ status: 'error', message: '[attractions] Failed to parse attractions data', raw_response: cleaned });
		}

		if (data.error) {
			return res.status(400).json({ status: 'error', message: data.error });
		}

		const attractions = Array.isArray(data.attractions) ? data.attractions : [];

		const normalized = attractions
			.filter((a) => a && typeof a === 'object')
			.map((a) => ({
				name: a.name,
				type: a.type && String(a.type).toLowerCase(),
				description: a.description,
				latitude: a.latitude !== undefined && a.latitude !== null ? Number(a.latitude) : null,
				longitude: a.longitude !== undefined && a.longitude !== null ? Number(a.longitude) : null,
				opening_time: a.opening_time,
				closing_time: a.closing_time,
				cost_amount: a.cost_amount !== undefined ? Number(a.cost_amount) : undefined,
				cost_currency: a.cost_currency,
				cost_note: a.cost_note,
				booking_required: Boolean(a.booking_required),
				booking_website: a.booking_website || null,
				famous_for: titleCase(a.famous_for || ''),
			}))
			.filter((a) => a.name && a.type && ALLOWED_TYPES.includes(a.type))
			.filter((a) => !foodPatterns.test(a.type) && !foodPatterns.test(a.name || ''));

		// Replace/ensure coordinates via Nominatim so they are accurate.
		await enrichAttractionsWithNominatimCoordinates({ city, attractions: normalized });
		for (const a of normalized) {
			if (!a || typeof a !== 'object') continue;
			if (!isValidLatLng(a.latitude, a.longitude)) {
				a.latitude = null;
				a.longitude = null;
			}
		}

		return res.json({ status: 'ok', city, attractions: normalized });
	} catch (err) {
		console.error('[attractions] Error:', err);
		return res.status(500).json({ status: 'error', message: '[attractions] Internal server error' });
	}
});

module.exports = { router };
